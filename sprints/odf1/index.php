<h3>What is the purpose of this weekend?</h3>
<p>
With the release of KOffice 2.0 (and KDE4.0!) drawing closer every day, KOffice wishes to integrate itself further with the OpenDocument Format (ODF) standard. As a result, a meeting has been organised for representatives from each KOffice application in order to further merge KOffice with the ODF format and create a bigger and better standard for the office suite. It is hoped that this meeting will shape and define the future of ODF within KOffice, and KDE in general.
</p>
