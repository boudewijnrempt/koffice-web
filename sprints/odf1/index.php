From <strong>Saturday the 12th of May 2007 until Sunday the 13th</strong>, KOffice is organising an <a href="http://docs.oasis-open.org/office/v1.1/OS/OpenDocument-v1.1.pdf">ODF</a> "hackathon" weekend to be held at <strong>Berlin's KDAB offices</strong>.
