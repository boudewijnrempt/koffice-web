<h2>
  The KDE Project Ships KOffice 1.4.1 - a Free Integrated Office Suite
</h2>
<p align="justify">
<strong>
Improved Overall Stability, Better OASIS OpenDocument Support,
Krita gains Performance and the KOffice Workspace gets a new Icon.
</strong>
</p>
<p align="justify">July 25, 2005 (The INTERNET).
The <a href="http://www.kde.org/">KDE Project</a> today announced the
immediate release of KOffice 1.4.1 for Linux and Unix operating systems.
KOffice is an integrated set of free office applications based on the
<a href="http://www.kde.org/awards/">award winning</a> KDE desktop.
KOffice is a light-weight yet complete and feature rich office solution
and provides a variety of filters to interoperate with other popular
office suites such as OpenOffice.org and Microsoft Office. Among
various bugfixes and improvements, the KOffice 1.4.1 release further
improves support for the
<a href="http://en.wikipedia.org/wiki/OpenDocument">OASIS OpenDocument</a>
file format.
</p>

<h3>KOffice 1.4.1 consists of the following applications:</h3>
<ul>
<li><a href="../kword/">KWord</a> - A frame-based word processor</li>
<li><a href="../kspread/">KSpread</a> - An advanced spreadsheet application</li>
<li><a href="../kpresenter/">KPresenter</a> - A full-featured presentation program</li>
<li><a href="../kivio/">Kivio</a> - A Visio®-style flowcharting application</li>
<li><a href="../krita/">Krita</a> - A pixel based image manipulation application</li>
<li><a href="../kexi/">Kexi</a> - An integrated data management application</li>
<li><a href="../karbon/">Karbon14</a> - A vector drawing application</li>
<li><a href="../kugar/">Kugar</a> - A tool for generating business quality reports</li>
<li><a href="../kchart/">KChart</a> - An integrated graph and chart drawing tool</li>
<li><a href="../kformula/">KFormula</a> - A powerful formula editor</li>
</ul>
<p>
This full set of components and excellent desktop integration makes KOffice
one of the most complete office suites available.
</p>
<h3>
Major Improvements in the KOffice 1.4.1 release are:
</h3>
<ul>Improved OASIS OpenDocument Support, especially in KWord and KPresenter</ul>
<ul>Crash fixes for Kexi, Krita, KWord and KSpread</ul>
<ul>Krita Performance Improvements</ul>
<ul>Various other bugfixes</ul>
<li>
</ul>
<p>
Please take a look at the 
<a href="http://www.koffice.org/announcements/changelog-1.4.1.php">list of
changes</a> to see all the improvements made to KOffice since the
<a href="http://www.koffice.org/announcements/announce-1.4.php">1.4 release</a>.
</p>

