<h4>
  Compiling KOffice 1.2.1
</h4>
<p align="justify">
  <a name="source_code-library_requirements"></a><u>Library
  Requirements</u>.
  KOffice 1.2.1 requires the following libraries:
</p>
<ul>
  <li>
    <div align="justify">
      kdelibs 3.0, though kdelibs 3.0.5 is highly recommended.  Both KDE
      releases can be downloaded following the instructions in the
      respective press releases
      (<a href="http://www.kde.org/announcements/announce-3.0.html#package_locations">3.0</a>,
      <a href="http://www.kde.org/announcements/announce-3.0.5.html">3.0.5</a>)
      at the KDE website.  For more information on the KDE 3 series, please
      see the <a href="http://www.kde.org/announcements/announce-3.0.html">KDE
      3.0 press release</a>.
    </div>
  </li>
  <li>
    <div align="justify">
      Qt 3.0.3, which is available in source code from Trolltech as
      <a href="ftp://ftp.trolltech.com/qt/source/qt-x11-3.0.3.tar.gz">qt-x11-3.0.3.tar.gz</a>
      and in binary packages from most OS vendors, or later (Qt 3.0.5 is
      recommended).
    </div>
  </li>
  <li>
    <div align="justify">
      For reading help pages and other KOffice documentation,
      <a href="http://xmlsoft.org/">libxml2</a> &gt;= 2.4.9 and
      <a href="http://xmlsoft.org/XSLT/">libxslt</a> &gt;= 1.0.7.
    </div>
  </li>
</ul>
<p align="justify">
  <u>Compiler Requirements</u>.
  Please note that some components of
  KOffice 1.2.1 (such as the Quattro Pro import filter
  and <a href="http://www.koffice.org/kchart/">KChart</a>) will not
  compile with older versions of <a href="http://gcc.gnu.org/">gcc</a>
  or egcs, such as egcs-1.1.2 or gcc-2.7.2.  At a minimum gcc-2.95-* is
  required.
</p>
<p align="justify">
  <a name="source_code"></a><u>Source Code</u>.
  The complete source code for KOffice 1.2.1 is available for free download
  via one of the KDE
  <a href="http://download.kde.org/stable/koffice-1.2.1/src/">http</a>
  or <a href="http://www.kde.org/ftpmirrors.html">ftp</a> mirrors.
  These source packages have been digitally signed with
  <a href="http://www.gnupg.org/">GnuPG</a> using the David Faure
  PGP Key
  (available from the
  <a href="http://www.koffice.org/download/signature.php">KDE Signature page</a>
  or public key servers), and their respective MD5 sums are listed on the
  <a href="http://www.koffice.org/releases/1.2.1-release.php">KOffice
  1.2.1 Info Page</a>.
</p>
