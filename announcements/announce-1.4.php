<h2>
  The KDE Project Ships KOffice 1.4 - a Free Integrated Office Suite
</h2>
<p align="justify">
<strong>New Image Editor, New Database Management Application and OASIS OpenDocument
Support Highlight KOffice 1.4 Release for Linux and Other UNIXes</strong></p>
<p align="justify">June 21, 2005 (The INTERNET).
The <a href="http://www.kde.org/">KDE Project</a> today announced the
immediate release of KOffice 1.4 for Linux and Unix operating systems.
KOffice is an integrated set of free office applications based on the
<a href="http://www.kde.org/awards/">award winning</a> KDE desktop.
KOffice is a light-weight yet feature rich office solution and
provides a variety of filters to interoperate with other popular
office suites such as OpenOffice.org and Microsoft Office. In fact
the KOffice 1.4 release is a large step towards embracing the
<a href="http://en.wikipedia.org/wiki/OpenDocument">
OASIS OpenDocument</a> file format which has become an approved
standard for office file formats. This format is also used by
the upcoming <a href="http://www.openoffice.org">OpenOffice.org 2.0</a>,
thus providing high interoperability.
</p>
<p>
KOffice 1.4 consists of the following applications:</p>
<p>New applications in the 1.4 release:</p>
<ul>
<li><a href="../krita/">Krita</a> - A pixel based image manipulation application</li>
<li><a href="../kexi/">Kexi</a> - An integrated data management application</li>
</ul>
<p>Improved existing applications:</p>
<ul>
<li><a href="../kword/">KWord</a> - A frame-based word processor</li>
<li><a href="../kspread/">KSpread</a> - An advanced spreadsheet application</li>
<li><a href="../kpresenter/">KPresenter</a> - A full-featured presentation program</li>
<li><a href="../kivio/">Kivio</a> - A Visio®-style flowcharting application</li>
<li><a href="../karbon/">Karbon14</a> - A vector drawing application</li>
<li><a href="../kugar/">Kugar</a> - A tool for generating business quality reports</li>
<li><a href="../kchart/">KChart</a> - An integrated graph and chart drawing tool</li>
<li><a href="../kformula/">KFormula</a> - A powerful formula editor</li>
</ul>
<p>
This full set of integrated components makes KOffice one of the most
complete office suites available.
</p>
<p>
The greatest highlights in the 1.4 release are:
</p>
<ul>
<li><a href="../krita/">Krita</a> and <a href="../kexi/">Kexi</a>, two new applications.</li>
<li>Integration of the <a href="http://www.oasis-open.org/committees/tc_home.php?wg_abbrev=office">
OASIS OpenDocument</a> file format, which has become an accepted standard.</li>
</ul>
<p>
Other major improvements include a completely rewritten Excel filter
which can import Microsoft Excel files very well.
KChart has received many improvements, making it much more usable
and functional than before; KPresenter now supports master pages and
custom slide orders; KWord now offers much faster spell checking,
a new Word Perfect import filter and tooltip based autocompletion.
And Kivio has improved stencil support.
<br>
There were also many bugs fixed with notable ones like printing bugs in
KSpread as well as <a href="./changelog-1.4.php">many more</a>.
</p>
<p>
<strong>KOffice is the first office suite supporting the
OASIS OpenDocument to a large extent.</strong><br>
These filters are provided for thorough testing
while still using the KOffice native format as a solid and known to work choice
for documents. Switching to the OpenDocument format as the new native format is
planned for the next release when these filters are known to be rock solid and
functional. With this in mind the current KOffice release provides the
OASIS OpenDocument filters.
</p>

<h4>
  Principal Improvements
</h4>
<p align="justify">
  The principal improvements over KOffice 1.3 are:
</p>
    <strong>General</strong>:
    <ul type="disc">
      <li>
        <div align="justify">
        <strong>Krita</strong>:
Krita is an image editor and paint application with a wide range of
applications, from photo editing to the creation of original artwork.
Sporting a clear and uncluttered interface (for a paint app...) it makes the
features it delivers accessible and intuitive to use.
<br>
This is the first public release of Krita and while it provides a coherent set
of features, it is only the first stepping stone towards a flexible paint
application for artists and image editors.
<br>
For a list of features supported by Krita, see
 <a href="http://www.koffice.org/krita">the Krita information page</a>.
        </div>
      </li>
      <li>
        <div align="justify">
        <strong>Kexi</strong>.
KOffice 1.4 is the first official release to include Kexi.
Kexi is an integrated environment for creating and managing databases.
It can be used for creating database schemas; inserting data; performing
queries, and processing data. Forms can be created to provide a custom
interface to your data. All database objects - tables, queries and
forms - are stored in the database, making it easy to share databases.
<br>
Kexi is considered as a long awaited Open Source competitor for MS Access,
Filemaker and Oracle Forms. Its development is motivated by the lack of
Rapid Application Development (RAD) tools for database systems that are
sufficiently powerful, inexpensive, open standards driven and portable
across many OSes and hardware platforms.
<br>
For a list of features supported by Kexi, see
 <a href="http://www.kexi-project.org/wiki/wikiview/index.php?AnnouncementForKOffice1.4">the Kexi website</a>.
        </div>
      </li>
      <li>
        <div align="justify">
        <strong>OASIS OpenDocument support</strong>:
Most KOffice applications now support the <a href="http://www.oasis-open.org/committees/tc_home.php?wg_abbrev=office">OASIS OpenDocument</a> file format. KOffice developer David Faure is part of
the OASIS technical committee and was able to participate in the discussions that
led to the creation of this standard file format.
        </div>
      </li>
    </ul>
  <p />
    <strong>KWord (Word Processor)</strong>:
    <ul type="disc">
      <li>
        <div align="justify">
         rewritten spell-checking support which is much faster and solves many problems
         (e.g. KOffice-1.3's right-click-menu sometimes hanging); with aspell,
         spell-checking is available in more than 70 languages.
        </div>
      </li>
      <li>
        <div align="justify">
         word completion with tooltip;
        </div>
      </li>
      <li>
        <div align="justify">
         a new WordPerfect import filter, based on an external library, libwpd;
        </div>
      </li>
    </ul>
  <p />
    <strong>KPresenter (Presentation Tool)</strong>:
    <ul type="disc">
      <li>
        <div align="justify">
        a master page concept;
        </div>
      </li>
      <li>
        <div align="justify">
        reworked dialogs (property editor, object and page effects); and
        </div>
      </li>
      <li>
        <div align="justify">
        customizable slide order and selection for slide show.
        </div>
      </li>
    </ul>
  <p />
    <strong>KSpread (Spreadsheet)</strong>:
    <ul type="disc">
      <li>
        <div align="justify">
         Support for right-to-left spreadsheets;
        </div>
      </li>
      <li>
        <div align="justify">
        much improved value parsing and formatting;
        </div>
      </li>
      <li>
        <div align="justify">
        a calendar plug-in;
        </div>
      </li>
      <li>
        <div align="justify">
        additional options for cell validity, 5 new functions, and
        </div>
      </li>
      <li>
        <div align="justify">
        a new Microsoft Excel import filter.
        </div>
      </li>
    </ul>
  <p />
    <strong>KChart (Graph and chart drawing tool)</strong>:
    <ul type="disc">
      <li>
        <div align="justify">
        New, much improved, data editor;
        </div>
      </li>
      <li>
        <div align="justify">
        linear or logarithmic scale;
        </div>
      </li>
      <li>
        <div align="justify">
        printing support;
        </div>
      </li>
      <li>
        <div align="justify">
