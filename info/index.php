    <p><b>KOffice</b> is an integrated office suite for <a
    href="http://www.kde.org">KDE</a>.</p>

    <p>KOffice features a full set of applications
    which work together seamlessly to provide the best user experience 
    possible. 
    </p>

    <p>KOffice components work together. Every program in the KOffice
    suite is a component which adds more functionality to the whole.
    You can embed any KOffice component in any other KOffice component,
    this is realised using the KParts object model from
    <a href="http://www.kde.org">KDE</a>. </p>

    <p><b>KOffice</b>, like <a href="http://www.kde.org">KDE</a>, is a free project
    which is released under GPL-compatible open source licenses, e.g. GPL,
    LGPL and BSD.</p>

    <p>KOffice supports <a href="http://www.oasis-open.org/specs/index.php#opendocumentv1.1">Open Document Format</a> (ODF), an <a href="http://www.iso.org/iso/iso_catalogue/catalogue_tc/catalogue_detail.htm?csnumber=43485">ISO</a> standard. Infact, KOffice is the first office suite that used ODF as the default file format</p>

    <p>KOffice stays in active development. This means that KOffice
    is continually being improved, based on reports from users,
    evolving software technology and implementation of new
    features. Not all features are implemented yet in all
    applications. Some components are more complete than others,
    but many KOffice applications are already being used for
    productive professional work. If you want to make KOffice better, or
    just implement that one little feature you really need, don't
    hesitate to jump in and we will happily make your changes part
    of the next release for everyone to use and see.
    </p>

    <p>If you want to test the latest development versions of
    KOffice, you can download daily sources and install them. For
    instructions go <a href="/download/source.php">here</a>. Those
    parts of KOffice that are in the current release are also available
    in  <a href="/download/">pre-packaged binary form</a>
    and do not need to be compiled from
    source, unless you want the very latest versions.</p>

    <p>If you have problems compiling KOffice, please look at the
    <a href="/faq/">FAQs</a>, and when that fails, e-mail the
    <a href="mailto:koffice@kde.org">KOffice mailing list</a>. Please
    don't mail the authors. For reporting bugs please go to <a
    href="/contact/index.php#bugs">here</a>.</p>

    <p>Screenshots are placed per component, please look at the
    pages for each KOffice program. Alternatively, if you'd like to take
    a tour of KOffice and view some of it's more prominent features, please
    visit <a href="/tours/1.6/index.php">here</a></p>

