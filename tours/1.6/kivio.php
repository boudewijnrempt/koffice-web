<h2>Kivio</h2>
<p>
Kivio is an easy to use diagramming and flowcharting application with tight integration to the other KOffice applications. It enables you to create network diagrams, organisation charts, flowcharts and more. Some of the features included with Kivio in KOffice 1.6 include:
</p>
<ul>
	<li>A greatly improved and powerful text editing dialog</li>
	<li>A new docker which shows all objects on the page</li>
	<li>Automatic guidelines used for easy alignment</li>
</ul>
