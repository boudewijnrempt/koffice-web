<h2>KChart</h2>
<p>
KChart is a highly configurable, easy to use graph and chart drawing application, which can be used to embed charts into other KOffice documents, e.g. KWord reports or KPresenter slideshows. KChart also offers:
</p>
<ul>
	<li>Multiple types of graph: bar, line, pie, and more</li>
	<li>A flexible layout offering 16 legend/chart relationships to choose from</li>
	<li>Automatic axis computation based on data values</li>
</ul>
