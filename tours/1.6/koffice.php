<h2>What is KOffice?</h2>

<p>
KOffice is a revolutionary office package that combines every office application you'll ever need (and more!) into one package, integrating itself with other applications while maintaining good performance.
</p>

<h2>Why use KOffice?</h2>
<h3>Performance</h3>
<p>
KOffice's light footprint means it can run efficiently on even older machines, saving the user any hassle that may be caused by being forced to upgrade to newer hardware, all the while maintaining steady and reliable performance.
</p>
<h3>Inter-Application Compatability</h3>
<p>
Because KOffice works so well with other applications, you can even merge data from various KOffice applications and swap between them: for example, placing a KSpread spreadsheet inside a KWord word document.
</p>
<p>
KOffice is also proud to be fully compatible with the OpenDocument Format, a recognised ISO standard, so you no longer have to worry about any issues caused by operating system or application incompatibilities.
</p>

<h3>Everything you need</h3>
<p>
From word processing to databases; graphics editing to spreadsheets, KOffice contains all the applications you need to work efficiently, removing the hassle caused by downloading additional software each time you want to do something different.
</p>
