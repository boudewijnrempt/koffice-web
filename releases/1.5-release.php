 <p>
 The KOffice team is proud to announce what might be the most important 
 Open Source Software release for a long time: KOffice 1.5.  If you think
 that is taking things too far, then read on...
 </p>

 <h2>Native Support for the OpenDocument File Format</h2>
<p>
 The spread of the <a href="http://en.wikipedia.org/wiki/OpenDocument">OASIS
 OpenDocument File format (ODF)</a> is widely regarded as one of the most
 important developments in the whole IT industry right now. It will give 
 users world-wide the opportunity to control the future of their own
 data and also ensure that all documents can always be read at any
 time in the future.
</p>
<p>
 KOffice was the first to support, and now with 1.5 the second office suite to announce full support for OpenDocument as the default file format. It is also the first one not based on the OpenOffice.org code base, which makes ODF into a true industry standard. KOffice 1.5 is therefore the version that lets enterprises and organizations all over the world choose the office suite that fits their needs best.</p>
<p>
 KOffice 1.5 offers native ODF support for the important productivity applications KWord (odt), KSpread (ods), and KPresenter (odp).  In addition to that, the supporting applications KChart (.odc) and KFormula (.odf) also support ODF, although not yet fully.</p>
<p>
 Great care has been taken to ensure interoperability with other office software that supports OpenDocument, most notably OpenOffice.org.  We acknowledge, however, that its support and interoperability are not perfect yet. We hope to be able to quickly recognize and fix the incompatibilities that do exist in the upcoming 1.5.1 and 1.5.2 bugfix releases.
</p>

 <h2>Killer Applications</h2>
 <p>
  KOffice is the most comprehensive office suite in existence. In addition to the basic productivity applications, it also includes a creativity application suite featuring <a href="http://www.koffice.org/krita/">Krita</a>, a paint application with lots of image manipulation features, <a href="http://www.koffice.org/karbon/">Karbon</a>, a vector graphics application, and <a href="http://www.koffice.org/kivio/">Kivio</a>, a Visio&reg; style flowcharting application. Other components are <a href="http://www.koffice.org/kexi/">Kexi</a>, an integrated environment for creating database applications, and <a href="http://www.koffice.org/kplato/">KPlato</a>, a program for project management.</p>

 <p> 
 Even as parts of a suite, some of these applications are outstanding in their field:
 </p> 

 <ul>
 <li>
  <p>
  <b>Krita</b> is a full-featured pixel-based image application with layer support,
  full colorspace independence and a versatile 
  plug-in based architecture. Krita has advanced tremendously since
  the first released version and now sports: L*a*b and CMYK colorspaces, scripting,
  adjustment layers, layer groups and a first preview release of a natural
  media simulation colorspace for watercolors. 
  </p>
 </li>
 <li>
  <p>
  <b>Kexi</b> 1.0 is the much awaited data management application
  that is the KOffice counterpart to MS Access or FileMaker. Kexi integrates
  the data and GUI into one file that can be easily sent over the internet
  as an email attachment.
  </p>
  <p>
  Using Kexi, a new database project can be set up in less than one minute 
  as a single efficient file, without the need for administering servers. 
  When needed, MySQL or PostgreSQL servers can be used for storage, or 
  existing projects can be imported from MySQL or PostgreSQL servers or 
  MS Access files. The final version 
  focuses on the data exchangeability and server support.
 </p>
 </li>
 </ul>

 <h2>Extremely Fast Paced Development</h2>
 <p> 
  The KOffice team has been able to keep development speed very high. The reason for this is a very sound and easy to work with code base. It also builds on a solid foundation made up of the Qt library and the KDE base libraries.
 </p>
 <p>
  Despite having more components than any other office suite, things are improving very quickly.  Version 1.5 features OpenDocument as the default file format, introduction of a unified scripting framework, enhanced accessibility for users with disabilities, the first major release of Kexi and a technology preview of KPlato, a new project managment application.  You can read more about it in the <a href="/announcements/announce-1.5.php">full announcement</a>.
 </p>
 <p>
  KOffice has increased its developer base 100% from around 15 at the time of KOffice 1.4 to around 30 at the time of KOffice 1.5. We continue to see a healthy growth in our developer community, which bodes well for the future. If you want to make a difference, then consider joining the fun and excitement that is the KOffice development team.  This offer does not only apply for coders; we also need a healthy mix of documentors, graphics artists, usability and accessibility experts and web designers.
 </p>
 <p> 
  If we allow ourselves a look into the future, KOffice 2.0 will be released around the new year 2006/2007. It will build on the same platform as KDE 4.0, which means immediate portability to Windows and MacOS X. KOffice 2.0 will also include improved font handling, change management as well as improved integration.
 </p>
 <p>
  Given the development speed of KOffice, we expect most of the components to reach a best-of-breed status in the world of Open Source office programs in 1 to 2 years.
 </p>



