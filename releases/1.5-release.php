 <h1>The KOffice Project Announces version 1.5 Code Name Evening Star.</h1>

 <p>
 The KOffice team is proud to announce what might be the most important 
 Open Source Software release for a long time: KOffice 1.5.  You might ask 
 yourself why this is so.  Read on...
 </p>

 <h2>Native Support for the OpenDocument File Format</h2>
<p>
 The spread of the <a href="http://en.wikipedia.org/wiki/OpenDocument">OASIS
 OpenDocument File format (ODF)</a> is widely regarded as one of the most
 important developments in the whole IT industry right now. It will give 
 users world-wide the opportunity to control the future of their own
 documents and also ensure that all documents can always be read at any
 time in the future.
</p>
<p>
 KOffice was the first to support, and now with 1.5 is the second office suite to announce full support for OpenDocument as the default file format, and the first one not based on the OpenOffice.org code base, thus making ODF into a true standard. KOffice 1.5 is therefore the version that lets enterprises and organizations all over the world choose the office suite that fits their needs best.</p>
<p>
 KOffice 1.5 offers native ODF support for the important productivity applications KWord (odt), KSpread (ods), and KPresenter (odp).  In addition to that, the supporting applications KChart (.odc) and KFormula (.odf) also support ODF, although not yet fully.</p>
<p>
 Great care has been taken to ensure interoperability with other office software that supports OpenDocument, most notably OpenOffice.org.  We acknowledge, however, that its support and interoperability are not perfect yet. We hope to be able to quickly recognize and fix the incompatibilities that do exist in the upcoming 1.5.1 and 1.5.2 bugfix releases.
</p>

 <h2>Killer Applications</h2>
 <p>
 KOffice is the most comprehensive office suite in existence. In addition to the basic productivity applications, it also includes a creativity application suite featuring <a href="">Krita</a>, a paint application with lots of image manipulation features, <a href="">Karbon</a>, a vector graphics application and <a href="">Kivio</a>, a Visio&reg; style flowcharting application. It also includes <a href="">Kexi</a>, an integrated environment for creating database applications and <a href="">KPlato</a> a program for project management.</p>

 <p> 
 Even as part of a suite, some of these applications are outstanding in their field:
 </p> 

 <ul>
 <li>
  <p>
  <b>Krita</b> is posed to become the leading pixel based application in the
  open source world. It already has the best foundation of all free paint 
  programs and pixel editors with full color model indepency and its 
  plug-in based architecture. It does not yet have the largest number of
  features, filters and plug-ins but is acquiring them at a trememdous
  speed.
  </p>
  <p>
  With Krita 1.5, the gauntlet has been thrown. Let the battle of the 
  graphics scene begin.
  </p>
 </li>
 <li>
  <p>
  KOffice 1.5 contains the much expected final 1.0 version of Kexi - the data 
  management application that is the KOffice counterpart to MS Access or 
  FileMaker. 
  </p>
  <p>
  Using Kexi, a new database project can be set up in less than one minute 
  as a single efficient file, without a need for administering servers. 
  When needed, MySQL server can be used for storage or existing projects 
  can be imported from MySQL server of MS Access file. The final version 
  focuses on the data exchangeability and server support.
 </p>
 </li>
 </ul>

