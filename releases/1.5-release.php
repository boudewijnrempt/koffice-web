 <p>
 The KOffice team is proud to announce what might be the most important 
 Open Source Software release for a long time: KOffice 1.5.  If you think
 that is taking things too far, then read on...
 </p>

 <h2>Native Support for the OpenDocument File Format</h2>
<p>
 The spread of the <a href="http://en.wikipedia.org/wiki/OpenDocument">OASIS
 OpenDocument File format (ODF)</a> is widely regarded as one of the most
 important developments in the whole IT industry right now. It will give 
 users world-wide the opportunity to control the future of their own
 documents and also ensure that all documents can always be read at any
 time in the future.
</p>
<p>
 KOffice was the first to support, and now with 1.5 the second office suite to announce full support for OpenDocument as the default file format, and the first one not based on the OpenOffice.org code base. This makes ODF a true industry standard. KOffice 1.5 is therefore the version that lets enterprises and organizations all over the world choose the office suite that fits their needs best.</p>
<p>
 KOffice 1.5 offers native ODF support for the important productivity applications KWord (odt), KSpread (ods), and KPresenter (odp).  In addition to that, the supporting applications KChart (.odc) and KFormula (.odf) also support ODF, although not yet fully.</p>
<p>
 Great care has been taken to ensure interoperability with other office software that supports OpenDocument, most notably OpenOffice.org.  We acknowledge, however, that its support and interoperability are not perfect yet. We hope to be able to quickly recognize and fix the incompatibilities that do exist in the upcoming 1.5.1 and 1.5.2 bugfix releases.
</p>

 <h2>Killer Applications</h2>
 <p>
 KOffice is the most comprehensive office suite in existence. In addition to the basic productivity applications, it also includes a creativity application suite featuring <a href="http://www.koffice.org/krita/">Krita</a>, a paint application with lots of image manipulation features, <a href="http://www.koffice.org/karbon/">Karbon</a>, a vector graphics application, and <a href="http://www.koffice.org/kivio/">Kivio</a>, a Visio&reg; style flowcharting application. It also includes <a href="http://www.koffice.org/kexi/">Kexi</a>, an integrated environment for creating database applications, and <a href="http://www.koffice.org/kplato/">KPlato</a>, a program for project management.</p>

 <p> 
 Even as parts of a suite, some of these applications are outstanding in their field:
 </p> 

 <ul>
 <li>
  <p>
  <b>Krita</b> is a full-featured pixel-based image application with layer support,
  full colorspace independence and a versatile 
  plug-in based architecture. Krita has advanced tremendously since
  the first released version and now sports: L*a*b and CMYK colorspaces, scripting,
  adjustment layers, layer groups and a first preview release of a natural
  media simulation colorspace for watercolors. 
  </p>
 </li>
 <li>
  <p>
  <b>Kexi</b> 1.0 is the much expected final version of the data 
  management application that is the KOffice counterpart to MS Access or 
  FileMaker. 
  </p>
  <p>
  Using Kexi, a new database project can be set up in less than one minute 
  as a single efficient file, without the need for administering servers. 
  When needed, MySQL or PostgreSQL servers can be used for storage, or 
  existing projects can be imported from MySQL or PostgreSQL servers or 
  MS Access files. The final version 
  focuses on the data exchangeability and server support.
 </p>
 </li>
 </ul>

 <p>
  We expect the other components to reach similar status in the world of 
  Open Source office programs as well.
 </p>

