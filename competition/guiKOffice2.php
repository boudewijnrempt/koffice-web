<h3>Objectives</h3>
<p>The purpose of the competition is to generate new ideas for the user interface of the next generation of KOffice, KOffice 2, which is expected to be released around the same time as KDE 4.0.</p>
<p>Submissions to the competition should be GUI mockups accompanied with a written description of the intended workflow with ideas for the design of KOffice 2. The new GUI proposals should be for <a href="../kword">KWord</a>, <a href="../kspread">KSpread</a> and <a href="../kpresenter">KPresenter</a>.</p>

<h3>Prize</h3>
<p><b>$1000</b> (USD) will go to the best entry.</p>
<p>In case the judges cannot find an entry that is truly best the prize money may be split between at most 3 entries.</p>
<p>The prize donor wishes to remain anonymous. </p>
<p>Please note that any taxes due will be paid by the winners.</p>

<h3>Deadline</h3>
<p>All submissions should be sent by <b>15th February 2006</b> 11:59pm GMT to
<a href="mailto:koffice-competition AT kde
DOT org">koffice-competition@kde.org</a>. Please note that only the judges will
see those submissions.</p>
<p>You can send more than one entry.</p>
<p>You can also send an updated version of your submission before the deadline!</p>

<h3>Submission Format</h3> 
<p>The submissions should be in PDF or/and ODT (Open Document Text) format. See
the examples below.</p>
<p>Along with your mockup design you will add a description of your GUI and
stress out the major points of your design by explaining their strengths.</p>
<p>Please note that the current svn version of KWord can handle ODT!</p>

<h3>Example</h3>
Please see the example in each format:<br />
PDF: <a href="examples1/KOffice UI and Functionality Example.pdf">KOffice UI and
Functionality Example.pdf</a><br />
ODT: <a href="examples1/KOffice UI and Functionality Example.odt">KOffice UI and
Functionality Example.odt</a><br />
Please note that this example was written quickly and the judges expect more
details in your application!

<h3>Jury</h3>
<p>The judges will be picked from the KOffice developers. Currently, Inge Wallin and Boudewijn Rempt are appointed.</p>
<p>Date of announcement of the winner: <b>March 1st 2006</b></p>

<h3>Submitted Material</h3>
<p>All ideas, graphical elements, workflows, etc, should be free to be used in
KOffice 2.0 and published under the GNU General Public License, GPL.</p>

<p>You can use IRC on network <tt>irc.freenode.org</tt> port 6667, channel
