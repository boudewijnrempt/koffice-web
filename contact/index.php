<h2><a name="bugs">Bug Reporting</a></h2> 
       
    <p> 
        If you find a bug in KOffice, please go to the "Help" menu in the application, and choose "Report bug...". 
        Alternatively, please go to the <a href="http://bugs.kde.org">KDE Bugs database</a>. 
    </p> 
       
<h2><a name="lists">Mailing Lists</a></h2> 
     
    <p>KOffice has two general mailing lists:</p>

        <ul>
            <li><a href="http://www.kde.org/mailinglists/index.php#koffice">koffice mailing list</a></li>
            <li><a href="http://www.kde.org/mailinglists/index.php#koffice-devel">koffice-devel mailing list</a></li>
        </ul>

    <p>The koffice mailing list is mainly for the users of KOffice, the koffice-devel is mainly for the developers.
    Please note that the write access of the koffice-devel mailing list is restricted.</p>
    <p>As KOffice is part of KDE it uses some of KDE's mailing lists, for example
        <a href="http://www.kde.org/mailinglists/index.php#kde-i18n-doc">the kde-i18n-doc mailing list for the translators</a>.
    </p>
    <p>
    For Kexi-specific topics, <a href="http://kexi-project.org/wiki/wikiview/index.php?Support#Mailing_Lists">low traffic mailing lists</a> are provided.
    </p>
    <p>
        Please note that for historical reasons,
        <a href="http://www.kde.org/mailinglists/index.php#krita">Krita</a> and
        <a href="http://www.kde.org/mailinglists/index.php#kplato">KPlato</a>
        have their own mailing lists.
    </p>
    <p><a href="http://lists.kde.org">Archives of the mailing lists are available.</a></p>
 
